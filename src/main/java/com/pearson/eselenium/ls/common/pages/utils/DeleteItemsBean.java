public class DeleteItemsBean {
	private String itemType;
	private boolean isUnit;
	private String itemName;
	private String unitOrContentItem;
	
	public String getItemType() {
		return itemType;
	}
	public void setItemType(String itemType) {
		this.itemType = itemType;
	}
	public boolean isUnit() {
		return isUnit;
	}
	public void setUnit(boolean isUnit) {
		this.isUnit = isUnit;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getUnitOrContentItem() {
		return unitOrContentItem;
	}
	public void setUnitOrContentItem(String unitOrContentItem) {
		this.unitOrContentItem = unitOrContentItem;
	}
	
}
